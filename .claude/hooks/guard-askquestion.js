#!/usr/bin/env node
// PreToolUse hook (matcher: AskUserQuestion): запрет выпадающих опросников.
// AskUserQuestion — МОДАЛЬНЫЙ: блокирует текстовые ответы Заказчика (они висят под
// опросником, Claude их НЕ видит, пока не закрыт), даёт урезанные формулировки и
// провоцирует переспрашивать уже согласованное. Ломает низкофрикционный воркфлоу.
// Exit 2 = блок. Вопросы — обычным текстом с ❓; не критично → дефолт и дальше (Правила 7/8).

let raw = "";
process.stdin.on("data", (d) => (raw += d));
process.stdin.on("end", () => {
  let input;
  try { input = JSON.parse(raw); } catch (e) { process.exit(0); }
  // защита: блокируем только AskUserQuestion (даже если matcher шире)
  if (!/AskUserQuestion/i.test(String(input.tool_name || ""))) process.exit(0);
  process.stderr.write(
    "ЗАБЛОКИРОВАНО ХУКОМ: инструмент AskUserQuestion (выпадающий опросник) запрещён. " +
    "Он модальный и блокирует текстовые ответы Заказчика — они висят под ним, ты их НЕ видишь, " +
    "пока опросник не закрыт (Заказчик ответил текстом и ушёл, а ты не увидел). " +
    "Задай вопрос ОБЫЧНЫМ ТЕКСТОМ с маркером ❓ и полными формулировками. " +
    "А если вопрос НЕ критичен — возьми разумный дефолт и продолжай (Правила 7/8: не прерывай без нужды, очередь не ждёт)."
  );
  process.exit(2);
});
