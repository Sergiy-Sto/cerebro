#!/usr/bin/env node
// UserPromptSubmit hook: кодовое слово Заказчика («подумай», «обдумай», «а если подумать»…)
// → впрыскиваем процедуру Second Thought Gate на этот ход. mention-guard глушит УПОМИНАНИЯ слова
// (кавычки/дефис/слова-маркеры), чтобы цитата «подумай» не триггерила. Дополняет гейт в /plan.

let raw = "";
process.stdin.on("data", (d) => (raw += d));
process.stdin.on("end", () => {
  // ищем кодовое слово в тексте промпта (raw содержит JSON с полем prompt)
  // берём только текст промпта (raw — JSON; его кавычки-делимитеры «"подумай» сломали бы guard ниже)
  let prompt = raw;
  try { const j = JSON.parse(raw); if (j && typeof j.prompt === "string") prompt = j.prompt; } catch (e) {}
  const triggerRe = /подумай|обдумай|обдумать|поразмысли|а если подумать|глубже подума|second thought/i;
  // mention-guard: вырезаем УПОМИНАНИЯ слова «подумай» (в кавычках / через дефис / после слов-маркеров),
  // чтобы осталась только реальная КОМАНДА. Иначе цитата «подумай»/«подумай-граблей» ложно триггерит.
  const cleaned = prompt
    .replace(/[«»"'„“”‘’]\s*подума[а-яё]*|подума[а-яё]*\s*[«»"'„“”‘’]/gi, " ")
    .replace(/подума[а-яё]*\s*[-–—]/gi, " ")
    .replace(/(слов|фраз|режим|триггер|кодов|без|про)[а-яё]*\s+[«»"'„“”‘’]?подума[а-яё]*/gi, " ");
  if (triggerRe.test(cleaned)) {
    process.stdout.write(
      "=== РЕЖИМ «ПОДУМАЙ» (Second Thought Gate, запрошен Заказчиком) ===\n" +
        "Не выдавай первую правдоподобную мысль. Перед ответом по этому запросу пройди компактный отбор:\n" +
        "- Decision: суть решения одной строкой.\n" +
        "- 2 альтернативы: содержательно иные, не косметика.\n" +
        "- Слабейшее допущение: на чём всё держится / что сломает.\n" +
        "- Keep / Refine / Replace: выбор с обоснованием.\n" +
        "Задача — победить свою первую мысль, а не защитить её. Не можешь убедительно отстоять → пересобери.\n" +
        "⚑ ОБЯЗАТЕЛЬНО начни ответ видимым маркером-строкой «🧠 Deep Think активирован» — чтобы Заказчик видел, что режим сработал.\n" +
        "=== КОНЕЦ РЕЖИМА ===\n"
    );
  }
  process.exit(0);
});
